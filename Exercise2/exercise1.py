# 두 개의 값을 입력받아 사칙연산하는 프로그램
# 사용자 입력 받기
a = float(input("첫 번째 숫자를 입력하세요: "))
b = float(input("두 번째 숫자를 입력하세요: "))
# 사칙연산 결과 출력
print("덧셈 결과:", a + b)
print("뺄셈 결과:", a - b)
print("곱셈 결과:", a * b)
# 나눗셈은 0으로 나누는 경우를 방지
if b != 0:    
    print("나눗셈 결과:", a / b)
else:    
    print("나눗셈 결과: 0으로 나눌 수 없습니다.")